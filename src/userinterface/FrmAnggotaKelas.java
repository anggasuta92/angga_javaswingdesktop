    private void cmdAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAddActionPerformed
        // simpan mahasiswa ke dalam anggota kelas
        //inisialisasi table yang terseleksi
        int[] selectedRows = new int[tableMahasiswa.getSelectedRows().length];
        //tampung di variable selected rows
        selectedRows = tableMahasiswa.getSelectedRows();
        
        //looping ambil baris keberapa yang terseleksi
        for(int i = 0; i < selectedRows.length; i++){
            //simpan ke anggota
            AnggotaKelas anggota = new AnggotaKelas();
            anggota.setKodeKelas(kelas.getKode());
            anggota.setNim(String.valueOf(tableMahasiswa.getValueAt(selectedRows[i], 1)));
            try{
                DbAnggotaKelas.simpanData(anggota);
            }catch(Exception e){e.printStackTrace();}
        }
        
        isiMahasiswa();
        isiTerdaftar();
    }//GEN-LAST:event_cmdAddActionPerformed

