    public void isiTerdaftar(){
        modelTerdaftar.getDataVector().removeAllElements();
        modelTerdaftar.fireTableDataChanged();
        ArrayList lisTerdaftar = new ArrayList();
        try{
            lisTerdaftar = DbMahasiswa.tampil(0, 0, "nim in (select ak.nim from anggota_kelas ak where ak.kode_kelas='"+ kelas.getKode() +"')", "nama asc");
            if(lisTerdaftar!=null && lisTerdaftar.size()>0){
                for(int i = 0; i < lisTerdaftar.size(); i++){
                    Mahasiswa mhs = (Mahasiswa) lisTerdaftar.get(i);
                    Object[] o = new Object[3];
                    o[0] = (i+1);
                    o[1] = mhs.getNim();
                    o[2] = mhs.getNama();
                    modelTerdaftar.addRow(o);
                }
            }
        }catch(Exception e){e.printStackTrace();}
    }
    
    public void isiMahasiswa(){
        modelMhs.getDataVector().removeAllElements();
        modelMhs.fireTableDataChanged();
        ArrayList listMhs = new ArrayList();
        try{
            listMhs = DbMahasiswa.tampil(0, 0, "nim not in (select ak.nim from anggota_kelas ak inner join kelas k on ak.kode_kelas=k.kode "
                                                +" where k.kode_matakuliah='"+ kelas.getKodeMataKuliah() +"')", "nama asc");
            if(listMhs!=null && listMhs.size()>0){
                for(int i = 0; i < listMhs.size(); i++){
                    Mahasiswa mhs = (Mahasiswa) listMhs.get(i);
                    Object[] o = new Object[3];
                    o[0] = (i+1);
                    o[1] = mhs.getNim();
                    o[2] = mhs.getNama();
                    modelMhs.addRow(o);
                }
            }
        }catch(Exception e){e.printStackTrace();}
    }

