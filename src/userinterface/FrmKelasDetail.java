    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        if(txtKode.getText().length()<=0){
            JOptionPane.showMessageDialog(null, "Nim wajib diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        kelas.setKode(txtKode.getText());
        kelas.setNama(txtNama.getText());
        
        MataKuliah mataKuliah = new MataKuliah();
        try{
            mataKuliah = DbMataKuliah.tampilByNama(String.valueOf(cmbMataKuliah.getSelectedItem()));
        }catch(Exception e){}
        kelas.setKodeMataKuliah(mataKuliah.getKode());
        
        Dosen dosen = new Dosen();
        try{
            dosen = DbDosen.tampilByNama(String.valueOf(cmbDosen.getSelectedItem()));
        }catch(Exception e){}
        kelas.setNidn(dosen.getNidn());
        
        kelas.setHari(String.valueOf(cmbHari.getSelectedItem()));
        
        kelas.setJamMulai(Integer.parseInt(String.valueOf(cmbJamMulai.getSelectedItem())));
        kelas.setMenitMulai(Integer.parseInt(String.valueOf(cmbMenitMulai.getSelectedItem())));
        
        kelas.setJamBerakhir(Integer.parseInt(String.valueOf(cmbJamBerakhir.getSelectedItem())));
        kelas.setMenitBerakhir(Integer.parseInt(String.valueOf(cmbMenitBerakhir.getSelectedItem())));
        
        kelas.setRuangan(txtRuangan.getText());
        
        boolean isSaved = DbKelas.simpanData(kodeOld, kelas);
        if(isSaved){
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Infomasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed


