    public void loadData(){
        if( kelas.getKode().length()>0){
            txtKode.setText(kelas.getKode());
            txtKode.setEditable(false);
            txtNama.setText(kelas.getNama());
            
            MataKuliah mataKuliah = new MataKuliah();
            try{
                mataKuliah = DbMataKuliah.tampilByKode(kelas.getKodeMataKuliah());
            }catch(Exception e){}
            
            cmbMataKuliah.setSelectedItem(mataKuliah.getNama());
            
            Dosen dosen = new Dosen();
            try{
                dosen = DbDosen.tampilByNidn(kelas.getNidn());
            }catch(Exception e){}
            
            cmbDosen.setSelectedItem(dosen.getNama());
            cmbHari.setSelectedItem(kelas.getHari());
            
            String strJamMulai = String.valueOf(kelas.getJamMulai()<10 ? "0" + kelas.getJamMulai() : kelas.getJamMulai());
            String strMenitMulai = String.valueOf(kelas.getMenitMulai()<10 ? "0" + kelas.getMenitMulai() : kelas.getMenitMulai());
            String strJamBerakhir = String.valueOf(kelas.getJamBerakhir()<10 ? "0" + kelas.getJamBerakhir() : kelas.getJamBerakhir());
            String strMenitBerakhir = String.valueOf(kelas.getMenitBerakhir()<10 ? "0" + kelas.getMenitBerakhir() : kelas.getMenitBerakhir());
            cmbJamMulai.setSelectedItem(strJamMulai);
            cmbMenitMulai.setSelectedItem(strMenitMulai);
            cmbJamBerakhir.setSelectedItem(strJamBerakhir);
            cmbMenitBerakhir.setSelectedItem(strMenitBerakhir);
            txtRuangan.setText(kelas.getRuangan());
            
        }
    }
    
    public void isiListDosen(){
        ArrayList listDosen = new ArrayList();
        try{
            listDosen = DbDosen.tampil(0, 0, "", "nama asc");
            if(listDosen.size()>0){
                for(int i = 0; i < listDosen.size(); i++){
                    Dosen dosen = (Dosen) listDosen.get(i);
                    cmbDosen.addItem(dosen.getNama());
                }
            }else{
                cmbDosen.addItem("- tidak ada dosen -");
            }
        }catch(Exception e){}
        
    }
    
    public void isiListMataKuliah(){
        ArrayList listMataKuliah = new ArrayList();
        try{
            listMataKuliah = DbMataKuliah.tampil(0, 0, "", "nama asc");
            if(listMataKuliah.size()>0){
                for(int i = 0; i<listMataKuliah.size(); i++){
                    MataKuliah mataKuliah = (MataKuliah) listMataKuliah.get(i);
                    cmbMataKuliah.addItem(mataKuliah.getNama());
                }
            }else{
                cmbMataKuliah.addItem("- tidak ada mata kuliah -");
            }
        }catch(Exception e){}
        
    }
    
    public void isiListWaktu(){
        //mengisi jam
        for(int jam=0; jam<=23; jam++){
            String strJam = "0" + String.valueOf(jam);
            if(jam>9) strJam = String.valueOf(jam);
            
            cmbJamMulai.addItem(strJam);
            cmbJamBerakhir.addItem(strJam);
        }
        
        //mengisi menit
        for(int menit=0; menit<=59; menit++){
            String strMenit = "0" + String.valueOf(menit);
            if(menit>9) strMenit = String.valueOf(menit);
            
            cmbMenitMulai.addItem(strMenit);
            cmbMenitBerakhir.addItem(strMenit);
        }
    }
    
