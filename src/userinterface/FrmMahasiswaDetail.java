    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        if(txtNim.getText().length()<=0){
            JOptionPane.showMessageDialog(null, "Nim wajib diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        mahasiswa.setNim(txtNim.getText());
        mahasiswa.setNama(txtNama.getText());
        mahasiswa.setTempatLahir(txtTempatLahir.getText());
        mahasiswa.setTanggalLahir((Date) txtTanggalLahir.getValue());
        String jenisKelamin = "P";
        if(rLakiLaki.isSelected()){
            jenisKelamin = "L";
        }
        mahasiswa.setJenisKelamin(jenisKelamin);
        mahasiswa.setProgramStudi(txtProdi.getSelectedItem().toString());
        mahasiswa.setJurusan(txtJurusan.getSelectedItem().toString());
        mahasiswa.setTahunMasuk(Integer.parseInt(txtTahunMasuk.getText()));
        boolean isSaved = DbMahasiswa.simpanData(nimOld, mahasiswa);
        if(isSaved){
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Infomasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed


