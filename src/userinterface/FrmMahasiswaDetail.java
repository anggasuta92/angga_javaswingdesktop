    private void txtProdiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_txtProdiItemStateChanged
        String selected = txtProdi.getModel().getSelectedItem().toString();
        txtJurusan.removeAllItems();
        if(selected.equalsIgnoreCase("Sistem Komputer")){
            txtJurusan.addItem("SK");
        }else{
            txtJurusan.addItem("MDI");
            txtJurusan.addItem("DGM");
            txtJurusan.addItem("KAB");
        }
        //JOptionPane.showMessageDialog(rootPane, "berubah");
    }//GEN-LAST:event_txtProdiItemStateChanged

