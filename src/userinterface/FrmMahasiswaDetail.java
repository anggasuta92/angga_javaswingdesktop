    public void loadData(){
        if(mahasiswa.getNim().length()>0){
            //jika dia edit maka tampilkan data di text field dan lain lain
            txtNim.setText(mahasiswa.getNim());
            txtNama.setText(mahasiswa.getNama());
            txtTempatLahir.setText(mahasiswa.getTempatLahir());
            txtTanggalLahir.setValue(mahasiswa.getTanggalLahir());
            if(mahasiswa.getJenisKelamin().equalsIgnoreCase("L")){
                rLakiLaki.setSelected(true);
            }else{
                rPerempuan.setSelected(true);
            }
            
            txtProdi.setSelectedItem(mahasiswa.getProgramStudi());
            txtJurusan.setSelectedItem(mahasiswa.getJurusan());
            
            txtTahunMasuk.setText(String.valueOf(mahasiswa.getTahunMasuk()));
        }
    }
    
